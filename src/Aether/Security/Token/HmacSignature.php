declare(strict_types=1);

namespace Aether\Security\Token;

final class HmacSignature {

    private const HASH_ALGORITHM = "sha256";

    /**
     * @param string $_signature
     * @param string $_json
     *
     * @return bool
     */
    public static function _hmacEquals(string $_signature, string $_json){
        return hash_equals($_signature, hash_hmac(self::HASH_ALGORITHM, $_json, $_ENV["SESSION_HMAC"]));
    }
}